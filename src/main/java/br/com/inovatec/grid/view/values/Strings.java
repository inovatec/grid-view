    public static String getDialogTitle(String complement) {
        return getReplacedString(Strings.DIALOG_TITLE, complement);
    }
    
    public static String getReplacedString(String value, String complement) {
        return value.replace("$1", complement);
    }
    
    public static String getReplacedString(String value, String... complement) {
        for (int i = 0; i < complement.length; i++) {
            value = value.replace("$" + (i + 1), complement[i]);
        }
        return value;
    }
    
